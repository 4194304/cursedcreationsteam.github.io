function menuopen() {
    document.getElementById('menu')
    .style.display = "inline";
    document.getElementById('navitemclose')
    .style.display = "inline";
    document.getElementById('menu')
    .style.animationName = "menuopen";
}  
function menuclose() {
    document.getElementById('menu')
    .style.display = "none";
    document.getElementById('navitemclose')
    .style.display = "none";
}  
